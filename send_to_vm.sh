#!/bin/bash

# ═══════════════════════════════════════════════════════════════════
#  Скрипт для ноутбука: отправка файлов на виртуальную машину
# ═══════════════════════════════════════════════════════════════════

echo "=========================================="
echo "  Отправка Yukitery на VM"
echo "=========================================="

YELLOW='\033[1;33m'
GREEN='\033[0;32m'
NC='\033[0m'

# Запрос IP VM
echo "Введите IP адрес виртуальной машины:"
read -r VM_IP

# Запрос пользователя VM
echo "Введите用户名 (обычно 'root' или ваше имя):"
read -r VM_USER

# Проверка соединения
echo -e "${YELLOW}Проверка соединения с VM...${NC}"
if ping -c 1 "$VM_IP" > /dev/null 2>&1; then
    echo -e "${GREEN}VM доступна!${NC}"
else
    echo -e "${YELLOW}VM не пингуется, но попробуем продолжить...${NC}"
fi

# Определение директории проекта (текущая директория скрипта)
SOURCE_DIR="$(cd "$(dirname "$0")" && pwd)"

echo ""
echo "Локальная директория: $SOURCE_DIR"
echo "Удалённая директория: /root/yukitery"
echo ""

# Копирование файлов
echo -e "${YELLOW}Копирование файлов на VM...${NC}"

# Используем scp для копирования
scp -r "$SOURCE_DIR"/* "${VM_USER}@${VM_IP}:/root/yukitery/"

echo -e "${GREEN}Файлы скопированы!${NC}"
echo ""
echo "Теперь перейдите в терминал VM и выполните:"
echo "  bash /root/yukitery/install_vm.sh"
